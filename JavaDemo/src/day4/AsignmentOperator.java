package day4;

public class AsignmentOperator {

	public static void main(String[] args) {
		//5.Assignment Operators(= += -+ *= /= %=)
		int a=10;
		/*a=a+5;
		System.out.println("Value of a is "+a); //output will be 15
		*/
		
		a+=5;
		System.out.println("Value of a by using Assignment Operator is:"+a);
		
		a-=5;
		System.out.println(a);
		
		a*=5;
		System.out.println(a);
		
		a/=5;
		System.out.println(a);
		
		a%=3;
		System.out.println(a);
		
		

	}

}
